package com.company;



public class Rectangle {
    public static void main(String[] args) {
        int a = 10;
        int b = 6;

        int perimeter = 2 * a + 2 * b;
        int area = a * b;

        System.out.println("Rectangle's perimeter is " + perimeter + "cm");
        System.out.println("Rectangle's area is " + area + " cm^2");

    }

}
